    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));//Fileter the file
        FileNameExtensionFilter filter  = new FileNameExtensionFilter("*.Images","jpg","gif","png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        //if the user click on save in the JfileChooser
        if(result == JFileChooser.APPROVE_OPTION)
        {
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            txtImage.setIcon(ResizeImage(path, null));
                    //setIcon(ResizeImage(path,null));
            imgpath = path;
        }
        else if(result  == JFileChooser.CANCEL_OPTION)
        {
            System.out.println("No file select");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        
        String code = jTable1.getValueAt(r, 0).toString();
        String name = jTable1.getValueAt(r, 1).toString();
        String chef = jTable1.getValueAt(r, 2).toString();
        String selection = jTable1.getValueAt(r, 3).toString();
      // Blob image = (Blob) jTable1.getValueAt(r, 4);
        
        txtname.setText(name);
        txtCode.setText(code);
        txtChef.setText(chef);
        txtSection.setText(selection);
       //txtImage.setIcon(image);
        Connection c;
       String sql = "select ItemImage from bakeryItems where ItemCode = '"+ code +"'";
        try {
            c = DB.mycon();
               java.sql.Statement s = c.createStatement();
               
               rs = s.executeQuery(sql);
               if(rs.next())
               {
                   txtImage.setIcon(ResizeImage(null, rs.getBytes("ItemImage")));
               }
               else
               {
                   JOptionPane.showMessageDialog(null, "No Image To Show");
               }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddNewItem.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddNewItem.class.getName()).log(Level.SEVERE, null, ex);
        }
                 
        
        
    }//GEN-LAST:event_jTable1MouseClicked

