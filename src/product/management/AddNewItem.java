    public ImageIcon ResizeImage (String ImagePath, byte[] pic)
    {
        ImageIcon MyImage = null;
        if(ImagePath != null)
        {
            MyImage = new ImageIcon(ImagePath);
            
        }
        else
        {
            MyImage = new ImageIcon(pic);
            
        }
        Image img = MyImage.getImage();
        Image nwImage = img.getScaledInstance(txtImage.getWidth(), txtImage.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(nwImage);
        return image;
    }

    
